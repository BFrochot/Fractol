#include <math.h>

int		norme(int *v, int *w)
{
	return (sqrt((v[0] - w[0]) * (v[0] - w[0]) + (v[1] - w[1]) * (v[1] - w[1])
			+ (v[2] - w[2]) * (v[2] - w[2])));
}

int		scal_3d(int *v, int *w)
{
	return (v[0] * w[0] + v[1] * w[1] + v[2] * w[2]);
}
